#!/bin/bash

############################################
# 開始処理
############################################
echo "======= 言語・時刻設定開始 ======="
date
sudo su -



############################################
# 言語設定
############################################
dnf install -y glibc-langpack-ja
localectl set-locale ja_JP.UTF-8



############################################
# 時刻の設定
############################################
timedatectl set-timezone Asia/Tokyo



############################################
# 終了処理
############################################
date
echo "======= 言語・日時設定完了 ======="
exit 0
